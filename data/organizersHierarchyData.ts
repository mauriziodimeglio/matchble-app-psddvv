/**
 * Helper function to get all organizers
 */
export function getAllOrganizers(): FirestoreOrganizer[] {
  return organizersHierarchy;
}

/**
 * Helper function to get organizer by ID
 */
export function getOrganizerById(id: string): FirestoreOrganizer | undefined {
  return organizersHierarchy.find(org => org.id === id);
}

/**
 * Helper function to get children organizers
 */
export function getChildrenOrganizers(parentId: string): FirestoreOrganizer[] {
  return organizersHierarchy.filter(org => org.parentId === parentId);
}

/**
 * Helper function to get organizers by level
 */
export function getOrganizersByLevel(level: TerritorialLevel): FirestoreOrganizer[] {
  return organizersHierarchy.filter(org => org.level === level);
}

/**
 * Helper function to get organizers by region
 */
export function getOrganizersByRegion(region: string): FirestoreOrganizer[] {
  return organizersHierarchy.filter(org => org.territory.region === region);
}

/**
 * Helper function to get organizers by province
 */
export function getOrganizersByProvince(province: string): FirestoreOrganizer[] {
  return organizersHierarchy.filter(org => org.territory.province === province);
}

/**
 * Helper function to get root organizers (national level)
 */
export function getRootOrganizers(): FirestoreOrganizer[] {
  return organizersHierarchy.filter(org => org.level === 'nazionale' && !org.parentId);
}

/**
 * Helper function to get full hierarchy path for an organizer
 */
export function getOrganizerHierarchy(organizerId: string): FirestoreOrganizer[] {
  const hierarchy: FirestoreOrganizer[] = [];
  let currentOrg = getOrganizerById(organizerId);
  
  while (currentOrg) {
    hierarchy.unshift(currentOrg);
    if (currentOrg.parentId) {
      currentOrg = getOrganizerById(currentOrg.parentId);
    } else {
      break;
    }
  }
  
  return hierarchy;
}
